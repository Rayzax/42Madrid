#include <unistd.h>

int	main(int c, char **v)
{
	int	i;
	int	n;

	i = 1;
	n = 0;
	while (c > i)
	{
		while (v[i][n] != '\0')
		{
			write(1, &v[i][n], 1);
			n++;
		}
		write(1, "\n", 1);
		i++;
		n = 0;
	}
	return (0);
}
